(function() {
	// Load plugin specific language pack
	tinymce.PluginManager.requireLangPack('groupdocs');

	tinymce.create('tinymce.plugins.GroupdocsPlugin', {
		/**
		 * Initializes the plugin, this will be executed after the plugin has been created.
		 * This call is done before the editor instance has finished it's initialization so use the onInit event
		 * of the editor instance to intercept that event.
		 *
		 * @param {tinymce.Editor} ed Editor instance that the plugin is initialized in.
		 * @param {string} url Absolute URL to where the plugin is located.
		 */
		init : function(ed, url) {
			// Register the command so that it can be invoked by using tinyMCE.activeEditor.execCommand('mceGroupdocs');
			ed.addCommand('mceGroupdocs', function() {
				ed.windowManager.open({
					file : url + '/groupdocs.htm',
					width : 320 + parseInt(ed.getLang('groupdocs.delta_width', 0)),
					height : 200,// + parseInt(ed.getLang('groupdocs.delta_height', 0)),
					inline : 1
				}, {
					plugin_url : url, // Plugin absolute URL
					some_custom_arg : 'custom arg' // Custom argument
				});
			});

			// Register groupdocs button
			ed.addButton('groupdocs', {
				title : 'groupdocs.desc',
				cmd : 'mceGroupdocs',
				image : url + '/img/groupdocs.gif'
			});

			// Add a node change handler, selects the button in the UI when a image is selected
			ed.onNodeChange.add(function(ed, cm, n) {
				cm.setActive('groupdocs', n.nodeName == 'IMG');
			});
		},

		/**
		 * Returns information about the plugin as a name/value array.
		 * The current keys are longname, author, authorurl, infourl and version.
		 *
		 * @return {Object} Name/value array containing information about the plugin.
		 */
		getInfo : function() {
			return {
				longname : 'Groupdocs plugin',
				author : 'Some author',
				authorurl : 'http://tinymce.moxiecode.com',
				infourl : 'http://wiki.moxiecode.com/index.php/TinyMCE:Plugins/groupdocs',
				version : "1.0"
			};
		}
	});

	// Register plugin
	tinymce.PluginManager.add('groupdocs', tinymce.plugins.GroupdocsPlugin);
})();